#include<stdio.h>

int main () {
        int Ganzzahl = 12345;
        float Gleitkommazahl = 123.45;
        char Character = 'a';
        char String[]="Das ist ein String";

        printf("Ganzzahl: %i \n", Ganzzahl);
        printf("Gleitkommazahl: %.2f\n", Gleitkommazahl);
        printf("Character: %c \n", Character);
        printf("String: %s \n", String);

        return 0;
}
